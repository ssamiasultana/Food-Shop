     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $product= new checkout();
        $product->name= $request->has('name')? $request->get('name'):'';
        $product->adress= $request->has('adress')? $request->get('adress'):'';
        $product->phone= $request->has('phone')? $request->get('phone'):'';
        $product->email=$request->has('email')? $request->get('email'):'';

        $product->save();
        return back()->with('success', 'Your Order is confrim!');
    }

